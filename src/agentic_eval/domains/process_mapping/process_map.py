"""Deterministic loaders for the synthetic process_mapping fixtures.

Two committed fixtures make up this domain's world:

- ``processes.jsonl`` — the GOLD process maps: for each business process, the
  ordered real steps (id, code, label, system-of-record, fix-before-automate
  flag, and reason code), plus the ordered handoff edges (handoff | decision,
  the system that carries the edge, and an optional decision branch label). This
  is the hand-authored source of truth / answer key (the analog of
  data_semantic's ``model.json``). It is read ONLY by ``compute.py`` (the answer
  key) and the tests — never surfaced to the agent by a tool.
- ``sources.jsonl`` — the messy multi-source FRAGMENTS: for each process, four
  fabricated sources (an email thread, a spreadsheet snippet, a portal
  click-path, a tribal-knowledge note) that mention ``STEP-##`` anchors and the
  evidence a mapper reasons from, but never the gold labels. These are the
  tools' only world (the analog of ``facts.jsonl``); they are deterministically
  generated by ``generate.py`` and byte-reproducible.

Everything is FABRICATED and GENERIC: generic retail/CPG buyer-side process
*shapes* only. No real company, process, system, deal, or figure is
represented. See ``fixtures/process_mapping/PROVENANCE.md``.
"""

from __future__ import annotations

import json
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any

from agentic_eval.domain import ROOT

FIXTURES = ROOT / "fixtures" / "process_mapping"
GOLD_PATH = FIXTURES / "processes.jsonl"
SOURCES_PATH = FIXTURES / "sources.jsonl"

# The system-of-record vocabulary a step may touch (the enum the agent chooses from).
SYSTEMS: frozenset[str] = frozenset(
    {"erp", "supplier_portal", "spreadsheet", "email", "manual", "edi", "wms", "tms", "bi"}
)

# The fix-before-automate reason vocabulary (an FBA step carries exactly one).
REASONS: frozenset[str] = frozenset({"UOM", "SOT", "JUDG", "DATA"})

# The fragment kinds a source may be.
SOURCE_KINDS: frozenset[str] = frozenset(
    {"email_thread", "spreadsheet", "portal_clickpath", "tribal_note"}
)


@dataclass(frozen=True)
class Step:
    """One real workflow step in a gold process map."""

    step_id: str  # STEP-##
    code: str  # snake_case slug
    label: str  # prose label
    system: str  # the ONE system of record it touches (from SYSTEMS)
    fix_before_automate: bool
    reason: str  # one of REASONS when FBA, else ""


@dataclass(frozen=True)
class Handoff:
    """One ordered edge between two steps; a decision edge chooses a branch."""

    frm: str  # STEP-## (JSON key is "from")
    to: str  # STEP-##
    kind: str  # "handoff" | "decision"
    via: str  # the system that carries the handoff
    label: str = ""  # the decision branch, when kind == "decision"


@dataclass(frozen=True)
class ProcessMap:
    """A single gold process: its ordered steps and ordered handoff edges."""

    process_id: str  # PROC-##
    title: str
    steps: tuple[Step, ...]
    handoffs: tuple[Handoff, ...]

    def step(self, step_id: str) -> Step:
        for s in self.steps:
            if s.step_id == step_id:
                return s
        raise KeyError(f"unknown step {step_id!r} in {self.process_id}")

    def edge(self, frm: str, to: str) -> Handoff:
        for h in self.handoffs:
            if h.frm == frm and h.to == to:
                return h
        raise KeyError(f"unknown edge {frm}->{to} in {self.process_id}")


@dataclass(frozen=True)
class SourceFragment:
    """One messy fragment: free text for the prose kinds, rows for a spreadsheet."""

    source_id: str  # SRC-###
    process_id: str  # PROC-##
    kind: str  # one of SOURCE_KINDS
    content: str = ""  # text kinds
    rows: tuple[tuple[str, ...], ...] = field(default_factory=tuple)  # spreadsheet kind


def _read_jsonl(path: Path) -> list[dict[str, Any]]:
    if not path.is_file():
        raise FileNotFoundError(f"no such fixture: {path}")
    return [json.loads(line) for line in path.read_text().splitlines() if line]


def _step_from_raw(raw: dict[str, Any]) -> Step:
    return Step(
        step_id=str(raw["step_id"]),
        code=str(raw["code"]),
        label=str(raw["label"]),
        system=str(raw["system"]),
        fix_before_automate=bool(raw["fix_before_automate"]),
        reason=str(raw.get("reason", "")),
    )


def _handoff_from_raw(raw: dict[str, Any]) -> Handoff:
    return Handoff(
        frm=str(raw["from"]),
        to=str(raw["to"]),
        kind=str(raw["kind"]),
        via=str(raw["via"]),
        label=str(raw.get("label", "")),
    )


def _process_from_raw(raw: dict[str, Any]) -> ProcessMap:
    return ProcessMap(
        process_id=str(raw["process_id"]),
        title=str(raw["title"]),
        steps=tuple(_step_from_raw(s) for s in raw["steps"]),
        handoffs=tuple(_handoff_from_raw(h) for h in raw["handoffs"]),
    )


def load_gold(path: Path = GOLD_PATH) -> dict[str, ProcessMap]:
    """Load the gold process maps keyed by process id (file order preserved)."""
    return {p.process_id: p for p in (_process_from_raw(r) for r in _read_jsonl(path))}


def _source_from_raw(raw: dict[str, Any]) -> SourceFragment:
    rows_raw = raw.get("rows", [])
    return SourceFragment(
        source_id=str(raw["source_id"]),
        process_id=str(raw["process_id"]),
        kind=str(raw["kind"]),
        content=str(raw.get("content", "")),
        rows=tuple(tuple(str(cell) for cell in row) for row in rows_raw),
    )


def load_sources(path: Path = SOURCES_PATH) -> dict[str, SourceFragment]:
    """Load the messy fragments keyed by source id (the tools' only world)."""
    return {s.source_id: s for s in (_source_from_raw(r) for r in _read_jsonl(path))}
